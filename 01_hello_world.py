# 01 - Hello World
# The classic first program. Python's print() function outputs text to the screen.

print("Hello, World!")

# You can print multiple values separated by commas.
# By default they are joined with a space.
print("Hello", "Python", "learner!")

# Use the sep parameter to change the separator.
print("one", "two", "three", sep=", ")

# Use the end parameter to change what is printed at the end of the line.
# By default it is a newline (\n).
print("First line", end=" | ")
print("Same line continued")

# Print an empty line.
print()

# Print a number directly.
print(42)

# Print the result of a calculation.
print(3 + 4)
